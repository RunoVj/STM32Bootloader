/**
* @brief This function handles System tick timer.
*/
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */

  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  HAL_SYSTICK_IRQHandler();
  /* USER CODE BEGIN SysTick_IRQn 1 */
	static uint8_t communication_counter;
	if (package_started) {
		++communication_counter;
		if (communication_counter == RECEIVE_TIMEOUT) {
			communication_counter = 0;
			
			if (package_received){
				package_received = false;          
				bool parsing_is_ok = parse_package(receive_buf, package_size);
				if (package_sended && parsing_is_ok){
					response(0xFF); 
				} 				
			}
			rx_counter = 0;
			HAL_UART_Receive_IT(&huart1, &rx_byte, 1);
			package_started = false;
		}
	}
	
	// blinking
	static uint8_t led_counter;
	if (led_counter == 50) {
		led_counter = 0;
		HAL_GPIO_TogglePin(DEBUG_LED_GPIO_Port, DEBUG_LED_Pin);
	}
	++led_counter;	
  /* USER CODE END SysTick_IRQn 1 */
}

